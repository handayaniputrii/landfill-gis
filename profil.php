	<center><b><h1>Profil Dinas Lingkungan Hidup dan Kebersihan kota Palembang</h1></b></center>
       <hr><br>
	<p style="text-align: justify; text-indent: 0.5in;">
	Dinas Lingkungan Hidup dan Kebersihan merupakan unsur pelaksana urusan pemerintahan bidang
	Lingkungan Hidup  serta sub urusan Persampahan. Dinas Lingkungan Hidup dan Kebersihan dipimpin oleh seorang
	Kepala Dinas yang berkedudukan di bawah dan bertanggung jawab kepada Walikota melalui Sekretaris Daerah. 
	Dinas Lingkungan Hidup dan Kebersihan mempunyai tugas membantu Walikota dalam melaksanakan urusan pemerintahan
	di bidang lingkungan hidup serta sub urusan persampahan dan tugas pembantuan.
	Dinas Lingkungan Hidup dan Kebersihan dalam melaksanakan tugas menyelenggarakan fungsi:</p>

	<p style="text-indent:0.5in;">a.	perumusan kebijakan teknis;<br>
	<p style="text-indent:0.5in;">b.	pelaksanaan kebijakan sesuai dengan lingkup tugasnya;<br>
	<p style="text-indent:0.5in;">c.	penyelenggaraan urusan pemerintahan dan pelayanan umum;<br>
	<p style="text-indent:0.5in;">d.	pelaksanaan evaluasi dan pelaporan sesuai dengan lingkup tugasnya;<br>
	<p style="text-indent:0.5in;">e.	pelaksanaan administrasi dinas sesuai dengan lingkup tugasnya; dan<br>
	<p style="text-indent:0.5in;">f.	pelaksanaan fungsi lain yang diberikan oleh Walikota terkait dengan tugas dan fungsinya.<br>
