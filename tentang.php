	<p style="text-align: justify; text-indent: 0.5in;">Berdasarkan data Dinas Lingkungan Hidup dan Kebersihan (DLHK) kota
	Palembang dalam sehari kota Palembang menghasilkan 600 - 700 ton sampah. 
	Bahkan saat hari libur bisa mencapai 900 ton per hari. Sungguh jumlah yang cukup besar,
	yang jika dibiarkan akan menjadi ancaman bagi kota Palembang, mengingat dampaknya 
	yang sangat serius bagi kesehatan dan lingkungan.</p>
	<p style="text-align: justify; text-indent: 0.5in;">Pengelolaan sampah yang kurang baik dan terbatasnya tempat 
	pembuangan sampah lah yang menjadi salah satu faktor penyebab menumpuknya sampah.
	Dengan adanya penambahan jumlah sampah menyebabkan TPA yang ada akan semakin penuh sehingga membutuhkan lokasi baru.</p> 
	<p style="text-align: justify; text-indent: 0.5in;">Tempat pembuangan sampah yang dibutuhkan ialah tempat pembuangan yang 
	memenuhi standar kelayakan sehingga tidak menyebabkan masalah lingkungan. Namun dalam kenyataannya, 
	penyediaan tempat pembuangan sampah ini terhambat oleh ketersediaan lahan. Apalagi di daerah perkotaan yang 
	ketersediaan lahan terbukanya sudah sangat sedikit.</p>
	<p style="text-align: justify; text-indent: 0.5in;">Dengan demikian dibuatlah web ini untuk
	menentukan lokasi tempat pembuangan sampah yang sesuai dengan standar yang ada dengan menggunakan metode pendukung
	keputusan <i>Multi-Objective Optimization On The Basis Of Ratio Analysis </i> (MOORA) yang kemudian akan di visualisasikan 
	berbentuk suatu pemetaan.</p>

